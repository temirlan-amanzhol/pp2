def above(movie):
    return movie ["imdb"] > 5.5

def filtered(movies):
    return [m for m in movies if m["imdb"] > 5.5]

def cate(movies, category):
    return [m for m in movies if m["category"].lower() == category.lower()]

def average(movies):
    return sum(m["imdb"] for m in movies) /len(movies)

def avrcate(movies, category):
    filt = cate(movies, category)
    return avrcate(filt)


